### 問題：神如何帶領以色列人離開埃及？

### 默想：神的故事
+ 在以色列人離開埃及的日子，神為了不要他們走回頭路，每天用雲柱跟火柱帶領他們。

+ 面對紅海，是以色列人一個很大的考驗。神再次施展大能，讓祂的子民經歷祂全然的信實跟拯救。

### 默想：我的故事
+ 【與神同行】 摩西離開埃及時，把約瑟的骸骨一同帶去；因為約瑟曾叫以色列人嚴嚴地起誓，對他們說：神必眷顧你們，你們要把我的骸骨從這裡一同帶上去。一個對神有信心的人，死了以後都還可以為神做見證。這給我們什麼提醒?

+ 【服事忠誠】 面對紅海，儘管眾人內心沮喪，對摩西充滿敵意，摩西卻鼓勵他們不要懼怕，看神所要施行的救恩。摩西對神的信心，在似乎陷於絕境時的積極態度，實在令人敬佩。我們的人生也免不了面對困境，我們如何可以學習對神這樣的信心?

